import argparse
import csv
import json
import random
from dataclasses import dataclass
from pathlib import Path

import cv2
import numpy as np
from PIL import Image


IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".tif", ".tiff", ".webp"}
MASK_EXTENSIONS = {".png", ".bmp", ".tif", ".tiff"}


@dataclass
class ItemRecord:
    stem: str
    image_path: Path
    mask_path: Path | None
    issues: list[str]
    include_in_split: bool


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Prepare img2img-turbo style datasets from source images and "
            "exported masks generated by X-AnyLabeling."
        )
    )
    parser.add_argument("--images-a", required=True, type=Path)
    parser.add_argument("--masks-a", required=True, type=Path)
    parser.add_argument("--images-b", required=True, type=Path)
    parser.add_argument("--masks-b", required=True, type=Path)
    parser.add_argument("--output-dir", required=True, type=Path)
    parser.add_argument(
        "--size",
        type=int,
        default=512,
        choices=[512, 1024],
        help="Output square size. 512 is recommended for img2img-turbo.",
    )
    parser.add_argument(
        "--background",
        default="black",
        choices=["black", "white", "transparent"],
        help="Background fill for composited training images.",
    )
    parser.add_argument(
        "--mask-threshold",
        type=int,
        default=127,
        help="Pixels above this threshold are treated as foreground.",
    )
    parser.add_argument(
        "--test-ratio",
        type=float,
        default=0.1,
        help="Fraction of accepted items sent to the test split.",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=42,
        help="Random seed for deterministic train/test splitting.",
    )
    parser.add_argument(
        "--min-foreground-ratio",
        type=float,
        default=0.003,
        help="Soft review threshold for tiny foreground masks.",
    )
    parser.add_argument(
        "--max-foreground-ratio",
        type=float,
        default=0.95,
        help="Soft review threshold for masks that cover almost the whole image.",
    )
    parser.add_argument(
        "--max-components",
        type=int,
        default=8,
        help="Soft review threshold for fragmented masks.",
    )
    parser.add_argument(
        "--exclude-flagged",
        action="store_true",
        help="Exclude soft-flagged items from train/test splits.",
    )
    return parser.parse_args()


def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def build_index(root: Path, allowed_exts: set[str]) -> dict[str, Path]:
    index: dict[str, Path] = {}
    for path in sorted(root.rglob("*")):
        if not path.is_file():
            continue
        if path.suffix.lower() not in allowed_exts:
            continue
        index[path.stem] = path
    return index


def load_mask_binary(mask_path: Path, threshold: int) -> np.ndarray:
    mask = cv2.imread(str(mask_path), cv2.IMREAD_GRAYSCALE)
    if mask is None:
        raise ValueError(f"Failed to read mask: {mask_path}")
    return np.where(mask > threshold, 255, 0).astype(np.uint8)


def inspect_mask(
    binary_mask: np.ndarray,
    min_foreground_ratio: float,
    max_foreground_ratio: float,
    max_components: int,
) -> list[str]:
    issues: list[str] = []
    foreground_ratio = float(np.count_nonzero(binary_mask)) / float(binary_mask.size)
    if foreground_ratio <= 0.0:
        issues.append("empty_mask")
        return issues
    if foreground_ratio < min_foreground_ratio:
        issues.append("tiny_foreground")
    if foreground_ratio > max_foreground_ratio:
        issues.append("oversized_foreground")

    components, _, stats, _ = cv2.connectedComponentsWithStats(
        (binary_mask > 0).astype(np.uint8), connectivity=8
    )
    component_count = max(components - 1, 0)
    if component_count > max_components:
        issues.append("fragmented_mask")

    if component_count > 0:
        areas = stats[1:, cv2.CC_STAT_AREA]
        if len(areas) > 1:
            dominant_ratio = float(np.max(areas)) / float(np.sum(areas))
            if dominant_ratio < 0.75:
                issues.append("multiple_large_parts")
    return issues


def resize_and_pad(
    image: Image.Image,
    mask: Image.Image,
    size: int,
    background: str,
) -> tuple[Image.Image, Image.Image]:
    src_w, src_h = image.size
    scale = min(size / src_w, size / src_h)
    new_w = max(1, int(round(src_w * scale)))
    new_h = max(1, int(round(src_h * scale)))

    image_resized = image.resize((new_w, new_h), resample=Image.Resampling.LANCZOS)
    mask_resized = mask.resize((new_w, new_h), resample=Image.Resampling.NEAREST)

    if background == "transparent":
        canvas = Image.new("RGBA", (size, size), (0, 0, 0, 0))
    elif background == "white":
        canvas = Image.new("RGBA", (size, size), (255, 255, 255, 255))
    else:
        canvas = Image.new("RGBA", (size, size), (0, 0, 0, 255))

    mask_canvas = Image.new("L", (size, size), 0)
    x = (size - new_w) // 2
    y = (size - new_h) // 2
    canvas.paste(image_resized, (x, y))
    mask_canvas.paste(mask_resized, (x, y))
    return canvas, mask_canvas


def composite_with_mask(
    image_path: Path,
    binary_mask: np.ndarray,
    size: int,
    background: str,
) -> tuple[Image.Image, Image.Image]:
    image = Image.open(image_path).convert("RGBA")
    mask_image = Image.fromarray(binary_mask, mode="L")
    canvas, mask_canvas = resize_and_pad(image, mask_image, size=size, background=background)

    alpha = mask_canvas
    if background == "transparent":
        composited = canvas.copy()
        composited.putalpha(alpha)
        return composited, mask_canvas

    bg_color = (255, 255, 255, 255) if background == "white" else (0, 0, 0, 255)
    bg = Image.new("RGBA", (size, size), bg_color)
    composited = Image.composite(canvas, bg, alpha)
    return composited.convert("RGB"), mask_canvas


def collect_domain_records(
    image_root: Path,
    mask_root: Path,
    threshold: int,
    min_foreground_ratio: float,
    max_foreground_ratio: float,
    max_components: int,
    exclude_flagged: bool,
) -> list[ItemRecord]:
    image_index = build_index(image_root, IMAGE_EXTENSIONS)
    mask_index = build_index(mask_root, MASK_EXTENSIONS)

    records: list[ItemRecord] = []
    for stem, image_path in image_index.items():
        issues: list[str] = []
        include_in_split = True
        mask_path = mask_index.get(stem)
        if mask_path is None:
            issues.append("missing_mask")
            include_in_split = False
            records.append(
                ItemRecord(
                    stem=stem,
                    image_path=image_path,
                    mask_path=None,
                    issues=issues,
                    include_in_split=include_in_split,
                )
            )
            continue

        try:
            binary_mask = load_mask_binary(mask_path, threshold)
        except Exception:
            issues.append("unreadable_mask")
            include_in_split = False
            records.append(
                ItemRecord(
                    stem=stem,
                    image_path=image_path,
                    mask_path=mask_path,
                    issues=issues,
                    include_in_split=include_in_split,
                )
            )
            continue

        issues.extend(
            inspect_mask(
                binary_mask=binary_mask,
                min_foreground_ratio=min_foreground_ratio,
                max_foreground_ratio=max_foreground_ratio,
                max_components=max_components,
            )
        )
        if "empty_mask" in issues:
            include_in_split = False
        elif exclude_flagged and issues:
            include_in_split = False

        records.append(
            ItemRecord(
                stem=stem,
                image_path=image_path,
                mask_path=mask_path,
                issues=issues,
                include_in_split=include_in_split,
            )
        )
    return records


def write_review_csv(path: Path, records: list[ItemRecord]) -> None:
    ensure_dir(path.parent)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(
            ["stem", "image_path", "mask_path", "include_in_split", "issues"]
        )
        for record in records:
            if not record.issues:
                continue
            writer.writerow(
                [
                    record.stem,
                    str(record.image_path),
                    "" if record.mask_path is None else str(record.mask_path),
                    int(record.include_in_split),
                    ",".join(record.issues),
                ]
            )


def split_records(
    records: list[ItemRecord], test_ratio: float, seed: int
) -> tuple[list[ItemRecord], list[ItemRecord]]:
    usable = [record for record in records if record.include_in_split]
    rng = random.Random(seed)
    rng.shuffle(usable)
    if not usable:
        return [], []
    test_count = int(round(len(usable) * test_ratio))
    if len(usable) > 1:
        test_count = max(1, min(test_count, len(usable) - 1))
    else:
        test_count = 0
    test_records = usable[:test_count]
    train_records = usable[test_count:]
    return train_records, test_records


def save_split(
    records: list[ItemRecord],
    image_out_dir: Path,
    mask_out_dir: Path,
    size: int,
    background: str,
    threshold: int,
) -> None:
    ensure_dir(image_out_dir)
    ensure_dir(mask_out_dir)
    for record in records:
        assert record.mask_path is not None
        binary_mask = load_mask_binary(record.mask_path, threshold)
        composited, mask_canvas = composite_with_mask(
            image_path=record.image_path,
            binary_mask=binary_mask,
            size=size,
            background=background,
        )
        image_out_path = image_out_dir / f"{record.stem}.png"
        mask_out_path = mask_out_dir / f"{record.stem}.png"
        composited.save(image_out_path)
        mask_canvas.save(mask_out_path)


def write_prompts(output_dir: Path) -> None:
    (output_dir / "fixed_prompt_a.txt").write_text(
        "a clean synthetic render of a spacecraft on a plain background\n",
        encoding="utf-8",
    )
    (output_dir / "fixed_prompt_b.txt").write_text(
        "a real photograph of a spacecraft on a plain background\n",
        encoding="utf-8",
    )


def write_summary(
    output_dir: Path,
    args: argparse.Namespace,
    records_a: list[ItemRecord],
    records_b: list[ItemRecord],
    train_a: list[ItemRecord],
    test_a: list[ItemRecord],
    train_b: list[ItemRecord],
    test_b: list[ItemRecord],
) -> None:
    summary = {
        "size": args.size,
        "background": args.background,
        "test_ratio": args.test_ratio,
        "seed": args.seed,
        "domain_a": {
            "images_root": str(args.images_a),
            "masks_root": str(args.masks_a),
            "total_images": len(records_a),
            "usable_images": len(train_a) + len(test_a),
            "train_count": len(train_a),
            "test_count": len(test_a),
            "flagged_count": sum(1 for record in records_a if record.issues),
        },
        "domain_b": {
            "images_root": str(args.images_b),
            "masks_root": str(args.masks_b),
            "total_images": len(records_b),
            "usable_images": len(train_b) + len(test_b),
            "train_count": len(train_b),
            "test_count": len(test_b),
            "flagged_count": sum(1 for record in records_b if record.issues),
        },
    }
    (output_dir / "review" / "summary.json").write_text(
        json.dumps(summary, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )


def main() -> None:
    args = parse_args()
    output_dir = args.output_dir
    review_dir = output_dir / "review"
    ensure_dir(output_dir)
    ensure_dir(review_dir)

    records_a = collect_domain_records(
        image_root=args.images_a,
        mask_root=args.masks_a,
        threshold=args.mask_threshold,
        min_foreground_ratio=args.min_foreground_ratio,
        max_foreground_ratio=args.max_foreground_ratio,
        max_components=args.max_components,
        exclude_flagged=args.exclude_flagged,
    )
    records_b = collect_domain_records(
        image_root=args.images_b,
        mask_root=args.masks_b,
        threshold=args.mask_threshold,
        min_foreground_ratio=args.min_foreground_ratio,
        max_foreground_ratio=args.max_foreground_ratio,
        max_components=args.max_components,
        exclude_flagged=args.exclude_flagged,
    )

    write_review_csv(review_dir / "review_A.csv", records_a)
    write_review_csv(review_dir / "review_B.csv", records_b)

    train_a, test_a = split_records(records_a, test_ratio=args.test_ratio, seed=args.seed)
    train_b, test_b = split_records(records_b, test_ratio=args.test_ratio, seed=args.seed)

    save_split(
        train_a,
        output_dir / "train_A",
        output_dir / "masks_A",
        size=args.size,
        background=args.background,
        threshold=args.mask_threshold,
    )
    save_split(
        test_a,
        output_dir / "test_A",
        output_dir / "masks_A",
        size=args.size,
        background=args.background,
        threshold=args.mask_threshold,
    )
    save_split(
        train_b,
        output_dir / "train_B",
        output_dir / "masks_B",
        size=args.size,
        background=args.background,
        threshold=args.mask_threshold,
    )
    save_split(
        test_b,
        output_dir / "test_B",
        output_dir / "masks_B",
        size=args.size,
        background=args.background,
        threshold=args.mask_threshold,
    )

    write_prompts(output_dir)
    write_summary(
        output_dir=output_dir,
        args=args,
        records_a=records_a,
        records_b=records_b,
        train_a=train_a,
        test_a=test_a,
        train_b=train_b,
        test_b=test_b,
    )

    print("Dataset preparation complete.")
    print(f"Output directory: {output_dir}")
    print(f"train_A: {len(train_a)}")
    print(f"test_A: {len(test_a)}")
    print(f"train_B: {len(train_b)}")
    print(f"test_B: {len(test_b)}")
    print(f"Flagged A: {sum(1 for record in records_a if record.issues)}")
    print(f"Flagged B: {sum(1 for record in records_b if record.issues)}")


if __name__ == "__main__":
    main()
